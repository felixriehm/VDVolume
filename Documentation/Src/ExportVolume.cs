/** \page pageExportVolume Export Volume
 * 
 * To export a <b>.vdvolume</b> file to PNG images follow these instructions:
 *
 * <ul>
 * <li> In the Unity editor find the menu item <b>VDVolume</b>. Click on it and click on entry <b>Export volume</b>.
 * <li> A new window opens where you have to <b>select the .vdvolume file</b>.
 * <li> <b>Select a destination folder</b> where the PNG images will be saved.
 * <li> Press <b>Export</b>
 * </ul>
 *
 * \image html vdvolume_menu.png "VDVolume menu" width=300px
 * \image html vdvolume_export.png "Export volume window" width=500px
 */