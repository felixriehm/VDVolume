/** \page pageImportImageSlices Import Image Slices
 * 
 * When importing images slices a volume file has to be created from the images. The file name ending of such files is <b>.vdvolume</b>.
 * To import images slices follow these instructions:
 *
 * <ul>
 * <li> In the Unity editor find the menu item <b>VDVolume</b>. Click on it and click on entry <b>Import image slices</b>.
 * <li> A new window opens where you have to <b>select the image folder</b>. For more information see the help text at the top of the window.
 * <li> <b>Select a destination folder</b> where the .vdvolume file will be saved.
 * <li> Press <b>Generate</b>
 * </ul>
 *
 * \image html vdvolume_menu.png "VDVolume menu" width=300px
 * \image html vdvolume_import.png "Import image slices window" width=700px
 */